// Declaração de uma função x expressão de uma função


// Declaração de uma função
// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function bomDia(){
    console.log('Bom dia!')
}

bomDia()
// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


// Expressão de uma função (function expressions)
// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
const boaNoite = function(){
    console.log('Boa noite!')
}

boaNoite()
// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// OBS: atribuir uma função que não retorna nenhum valor a uma variável dará resultado 'undefined'
let x = boaNoite() //undefined
console.log(x)
// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Porém, ao atribuir uma função que já retorna algum valor, não será mais undefined
const add = function(a, b){
    return a + b
}

let s = add(3, 4) //7
console.log(s)
// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

//Independente de quantos parâmetros adicionais forem necessários, o interpretador ignora todos os outros restantes
let resultado = add(3,4,5,6,7,8,9)
console.log(resultado) //7 

//Passando menos parâmetros
resultado = add(3)
console.log(resultado) //NaN (3 + undefined) - O segundo parâmetro (b) será undefined

// Parâmetros opcionais
const add2 = function(a = 3, b = 0){
    return a + b
} 

let  y = add2()
console.log(y)


